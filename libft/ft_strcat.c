char	*ft_strcat(char *dest, const char *src)
{
	int i;
	int j;

	j = -1;
	i = 0;
	while (dest[i])
		i++;
	while (src[++j])
		dest[j + i] = src[j];
	dest[j + i] = '\0';
	return (dest);
}
